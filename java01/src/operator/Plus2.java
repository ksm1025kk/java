package operator;

public class Plus2 {
	public static void main(String[] args) {
		// 숫자 더하기
	
		int 숫자1 = 10;
		int 숫자2 = 3;
		
		System.out.println(10 + 3);
		System.out.println("10 + 3");
		System.out.println(숫자1 + 숫자2);
		
		// 빼기
		System.out.println(10 - 3);
		System.out.println(숫자1 - 숫자2);
		
		// 곱하기
		System.out.println(10 * 3);
		System.out.println(숫자1 * 숫자2);
				
		// 나누기
		System.out.println(10 / 3);
		System.out.println(숫자1 / 숫자2);
						
		// 나머지 구하기
		System.out.println(10 % 3);
		System.out.println(숫자1 % 숫자2);
		
		int 숫자3 = 숫자1 * 숫자2;
		System.out.println("결과는 : "+숫자3);
				
				
	}
}
