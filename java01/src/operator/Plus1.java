package operator;

public class Plus1 {
	public static void main(String[] args) {
		// 연산자(연산기호) : 특정한 기능을 제공해주는 기호
		// + - * / % = > < >= <= == != ...
		
		// 문자열합치기(+)
		// "hello" + "world" 			//helloworld
		String 글자1 = "hello";
		String 글자2 = "world";
		String 글자3 = 글자1 + 글자2;
		System.out.println(글자3);
		System.out.println("helloworld");
		System.out.println(글자1 + 글자2);
		
		
	}
}
