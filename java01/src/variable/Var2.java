package variable;

public class Var2 {
	public static void main(String[] args) {
		// 변수 : 데이터값을 저장해놓는 공간
		// 변수(수학) : 변할 수 있는 수 x, y, z
		
		// JAVA가 변할 수 있기 때문에 변수에다가 저장해놓음
		String var1 = "python";
		System.out.println("HELLO "+var1+"!");
		System.out.println(var1+" IS SIMPLE");
		System.out.print("안녕하세요.");
		System.out.println("반갑습니다."+" ㅎㅎ");
		System.out.println("나는 "+var1);
		System.out.println(var1+"를 공부합니다.");
		System.out.println("");
		
		System.out.println(34-2);
		// 개발자가 32를 의도한건지 ,34-2 글자 그 자체를 의도한건지 컴퓨터는 알 수 없음
		// 사용자의 의도를 명확하게 나타내기 위해 변수에 자료형이라는 개념을 추가함
	}
}
