package cls;

public class Funtion {
	// main 함수(){}
	// 메서드(함수) : 코드를 저장해놓고 사용할 때 그 코드들은 실행
	// main 함수에 코딩을 하면 ==> 프로그램 실행시 main 함수 실행
	public static void main(String[] args) {
		// 자바에는 막혀있지만 '함수'라는 개념이 있습니다.
		// 함수 : 코드를 저장해놓는 공간
		// 변수 : 값을 저장하는 공간
		System.out.println();				// 출력해주는 함수
		// 자바에서는 함수를 직접적으로 사용하지 않고 클래스안에 담아서 "메서드"라는 개념 사용
	}
}
